# Ejemplo de como usar la función de split
# No olvides suscribirte a mision codigo en YouTube para
# mas tips y tutoriales de TIC

t = " Tenemos un texto sencillo y generico de ejemplo"

palabras = t.split()
print(palabras)

# Esta función nos permite especificar uno o mas caracteres
# para usar de separadores
lo = t.split("lo")
print(lo)

e2 = t.split("34")
print(e2)